            <div class="artical">
              <div class="header">
                <span class="return-link"  v-link="{ path: '/News-Share'}">返回上一层</span>
                <h2 class="title">银监会称理财新规仍在论证</h2>
                <span class="artical-date">2016-07-29</span><span class="artical-author">作者：吉梵金融</span>
              </div>
              <div class="paragraph">
                <p>银监会正在研究制定《商业银行理财业务监督管理办法》（以下简称《办法》），旨在推动银行理财业务规范转型，促进理财业务规范健康、可持续发展，有效防范风险。</p>
              </div>
              <div class="paragraph">
                <p>银监会称，《办法》目前处于内部研究论证和征求意见阶段，下一步，银监会将根据反馈意见对《办法》做进一步修订完善，并按照相关程序进一步广泛征求意见，根据各方意见修改完善后适时发布。</p>
              </div>
              <div class="paragraph">
                <p>根据此前部分媒体的报道，该《办法》的主要内容是对银行理财业务进行分类管理，禁止发行分级产品，以及对银行理财业务进行限制性投资。多位市场人士认为，在监管趋严的大背景下，银行理财市场的监管收紧符合预期。</p>
              </div>
              <div class="paragraph">
                <p>而在该《办法》的框架下，对于普通理财产品而言，在投资受限的情况下，未来相关银行理财产品的预期收益率或将继续下行。至于另一个颇受市场关注的新规对入市资金的影响，部分分析人士认为，此前市场对新规的影响或“反应过度”，目前银行理财入市资金规模有限，不应过度解读。</p>
              </div>
              <div class="paragraph">
                <p>收益下行或成大概率事件</p>
              </div>
              <div class="paragraph">
                <p>在《办法》中，颇受人关注的就是关于限制性投资的内容。新规指出，商业银行理财产品不得直接或间接投资于本行信贷资产及其受（收）益权，不得直接或间接投资于本行发行的理财产品，不得直接或间接投资于除货币市场基金和债券型基金之外的证券投资基金，不得直接或间接投资于境内上市公司公开或非公开发行或交易的股票及其受（收）益权，不得直接或间接投资于非上市企业股权及其受（收）益权。商业银行面向非机构客户发行的理财产品不得直接或间接投资于不良资产及其受（收）益权，银监会另有规定的除外。</p>
              </div>
              <div class="paragraph">
                <p>　此外，对于银行理财投资非标资产，新规也明确指出只能对接信托计划，而不能对接资管计划。不允许多层通道嵌套，鼓励银行理财发行一对一非标产品（无期限错配）。</p>
              </div>
              <div class="paragraph">
                <p>普益标准研究员魏骥遥认为，此前市场上普遍存在的多层通道嵌套的模式虽然较为常见，但可能导致较高的杠杆率且令监管难度增大。因此新规鼓励银行发行一对一的非标产品，无疑也扣紧了监管层希望简化监管各类产品或操作流程中的难度、防范未知风险的目的。</p>
              </div>
              <div class="paragraph">
                <p>但从实际操作层面而言，有市场分析人士指出，除了机构客户和私人银行客户等高净值客户外，普通客户购买的银行理财产品未来在投资领域中将有诸多限制，除了传统的债券类资产、货币及债券型基金和股票质押式回购等资产外，这类理财资金目前涉及的很多投资领域包括不良资产及其受（收）益权、定向增发、股票配资以及相关股权投资等，均将被拒之门外。</p>
              </div>
              <div class="paragraph">
                <p>而在当前无风险利率持续下行，货币和债券等资产收益率普遍不高的情况下，非标类资产和上述资产均是银行理财获取较高收益的“利器”。因此，有银行业内人士向记者坦言，在非标投资“通道”以及相关投资领域受限的情况下，未来银行理财产品的预期收益率下行将是大概率事件。</p>
              </div>
              <div class="paragraph">
                <p>股市或“反应过度”</p>
              </div>
              <div class="paragraph">
                <p>对于《办法》而言，另一个受人关注的问题便是对A股市场的影响。根据其内容，面向普通客户销售的理财产品将不能投资股票市场。这也意味着，这部分理财资金将无法入市。</p>
              </div>
              <div class="paragraph">
                <p>不过，记者在采访了多位业内人士之后了解到，《办法》中的新规对股市的入市资金虽有一定的影响，但其程度有限。在2015年上半年的牛市行情中，银行理财资金入市的形式主要有通过两融和收益互换为代表的配资，以及场外配资资金。其中，场外配资资金中又可细分为银行与券商和信托对接的配资、银行对私募等机构的配资以及银行对信托发行的结构化产品进行配资三大类。</p>
              </div>
              <div class="paragraph">
                <p>　而华泰证券(601688,股吧)在去年7月发布的一份研报曾估算：当时市场中通过两融渠道入市的理财资金大约在整个两融资金中占比为40%-45%；而场外配资的资金，对应前文所述的三大渠道，则分别为1500亿元、5000亿元和1500亿元。回顾去年下半年A股市场的“去杠杆”过程，场外配资中的第一大类入市资金已经在伞形信托的清理中基本出场，而其他途径入市的资金也因为股市的持续震荡而不断萎缩。比如，有银行业内人士向记者透露，通过信托等渠道对接私募配资需求入市的资金已经大幅下降，规模很可能已不及去年的一半。</p>
              </div>
              <div class="paragraph">
                <p>记者在采访相关信托业内人士时也了解到，当前信托的结构化配资业务虽然仍平稳开展，但来自各类客户的配资需求不高，入市资金量非常有限。来自用益信托的统计也显示，2016年上半年，包括阳光私募以及结构化配资业务在内的证券投资类信托业务总计成立了321款产品，成立规模为384.67亿元，同比去年上半年的2603.48亿元下降了85.22%。由此可见，通过信托渠道入市的资金规模非常有限。</p>
              </div>
              <div class="paragraph">
                <p>来自兴业证券(601377,股吧)的一份最新研报也显示，今年7月的第4周，在银行理财资金的基础资产中，股票类产品的占比仅为2.5%。因此，当前银行理财资金进入股市的量已经很小。</p>
              </div>
              <div class="paragraph">
                <p>　多位银行业内人士也向记者表示，新规中尚未提及对存量资金的处理方式，但按照惯例，应该是不会进行“一刀切”。综合各项因素，此前市场很可能是对新规“反应过度”，放大了其对市场的影响。</p>
              </div>
              <div class="paragraph">
                <p>近年来基金子公司、券商资管等机构加入竞争，对其冲击较大。新规只允许信托公司开展这一业务，该政策如若顺利落地，在客观上短期有利于信托行业增加业务收入，而其最大的价值在于给信托公司转型赢得了更多的时间。</p>
              </div>
              <div class="paragraph">
                <p>来源：中国证券网  </p>
              </div>
